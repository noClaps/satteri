//! Shared HTML utilities used by both serialization paths.

/// Returns `true` if the given HTML tag name is a void element
/// (no closing tag, no children allowed).
pub(crate) fn is_void_element(tag: &str) -> bool {
    matches!(
        tag,
        "area"
            | "base"
            | "br"
            | "col"
            | "embed"
            | "hr"
            | "img"
            | "input"
            | "link"
            | "meta"
            | "param"
            | "source"
            | "track"
            | "wbr"
    )
}
